package OutputPrograms;

public class Program14 {

	public static void main(String[] args) {
//Initializing the result 
		int result = 0;
		for (int i = 1; i < 100; i++) {

			result = result + i;

		}
		System.out.println("The sum of 100 =" + result);
	}

}
